    private void inicializar() {       
        cmbRopa.removeAllItems();   
        cmbRopa.addItem("Seleccione un item");
        for(Map.Entry<String, Float> entry : prendas.entrySet()) {
            cmbRopa.addItem(entry.getKey());

        }                    
        
        cmbTalle.removeAllItems();        
        cmbTalle.addItem("Seleccione un talle");
        for(Map.Entry<String, Float> entry : talles.entrySet()) {
            cmbTalle.addItem(entry.getKey());
        }
         cmbEstilo.removeAllItems();        
        cmbEstilo.addItem("Seleccione un estilo");
        for(Map.Entry<String, Float> entry : style.entrySet()) {
            cmbEstilo.addItem(entry.getKey());
        
        }    
            
        cmbRopa.setSelectedIndex(0);
        cmbTalle.setSelectedIndex(0);
        cmbEstilo.setSelectedIndex(0);
        
    }

    

    private float getPrecio(){
        String ropa = (String)cmbRopa.getSelectedItem();
        String talle = (String)cmbTalle.getSelectedItem();
        String estilo = (String)cmbEstilo.getSelectedItem();
        float precio = 0.0f;
        float mod_talle = 0.0f;
        float mod_estilo = 0.0f;
        for(Map.Entry<String, Float> itemPrenda : prendas.entrySet()) {
            if(itemPrenda.getKey().equals(ropa))
            {
                precio = itemPrenda.getValue();
                
            }
            
        }
        for(Map.Entry<String, Float> itemEstilo : style.entrySet()) {
            if(itemEstilo.getKey().equals(estilo))
            {
                mod_estilo = itemEstilo.getValue();                
            }            
        }
        
        for(Map.Entry<String, Float> itemTalle : talles.entrySet()) {
            if(itemTalle.getKey().equals(talle))
            {
                mod_talle = itemTalle.getValue();                
            }            
        }
        return  (precio * mod_talle * mod_estilo);
        
    }
    
    private void cambiaPrecio() {        
         txtPrecioAct.setText("" + getPrecio());
        
    }
