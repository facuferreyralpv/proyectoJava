    public void mostrarFactura(){
        String texto;
        try{
            FileReader lector = new FileReader("archivo.txt");
            FileReader lector2 = new FileReader("persona.txt");
            BufferedReader contenido = new BufferedReader(lector);
            BufferedReader contenido2 = new BufferedReader(lector2);
            while((texto=contenido.readLine())!=null )
                txtEfectivo.setText(txtEfectivo.getText()+"\n"+texto);
            while((texto=contenido2.readLine())!=null )
                txtEfectivo.setText(txtEfectivo.getText()+"\n"+texto);
            
            txtEfectivo.setText(txtEfectivo.getText()+"\n\nTotal: $"+Globales.getPrecioTotal());
            
        }catch(Exception e){
            
        }
        String s = Float.toString(Globales.getPrecioTotal());
        txtTotal.setText(s);}
    
    
