    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (getPrecio()!=0){

            String variable = txtTotal.getText();
            txtTotal.setText( variable +"Prenda: " + cmbRopa.getSelectedItem()
                + " - Estilo: " + cmbEstilo.getSelectedItem()
                + " - Talle: " + cmbTalle.getSelectedItem()
                + " - Precio: $" + getPrecio()+"\n");
            Globales.incrementarTotal(getPrecio());
        }
        else{
            
            JOptionPane.showMessageDialog(null,"No compĺeto su pedido","Error",JOptionPane.ERROR_MESSAGE);
        }

        try{
            if(getPrecio()!=0 ){
                File archivo = new File ("archivo.txt");
                FileWriter escribir = new FileWriter(archivo, true);
                escribir.write((String) "Ropa:   "+cmbRopa.getSelectedItem()+"\n"
                    + "Estilo:     "+cmbEstilo.getSelectedItem()+"\n"
                    + "Talle:     "+cmbTalle.getSelectedItem()+"\n"
                    +"Precio:    "+"$"+(int) getPrecio()+"\n"+"---------------------------------"+"\n");
                escribir.close();}

        }
        catch (Exception e){
            System.out.println("Nada");
        }

        art.add(new Articulo((String) cmbRopa.getSelectedItem(), (String) cmbEstilo.getSelectedItem(), (String) cmbTalle.getSelectedItem(), getPrecio()));

        inicializar();
    }//GEN-LAST:event_btnAgregarActionPerformed

