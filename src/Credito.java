    private void inicializar() {       
        cmbCuota.removeAllItems();   
        cmbCuota.addItem("Cuotas");
        for(Map.Entry<String, Float> entry : cuotas.entrySet()) {
            cmbCuota.addItem(entry.getKey());

        }       
        
     cmbCuota.setSelectedIndex(0);
    }


    private float getPrecio(){
        String ropa = (String)cmbCuota.getSelectedItem();
        float precio = 0.0f;
        for(Map.Entry<String, Float> itemCuota : cuotas.entrySet()) {
            if(itemCuota.getKey().equals(ropa))
            {
                precio = itemCuota.getValue();
                
            }
            
        }
        return (precio*Globales.getPrecioTotal());
    }
    
    private float getCuota(){
        String ropa = (String)cmbCuota.getSelectedItem();
        String precio;
        float f=0.0f;
        for(Map.Entry<String, Float> itemCuota : cuotas.entrySet()) {
            if(itemCuota.getKey().equals(ropa))
            {
                precio = itemCuota.getKey();
                f = Float.parseFloat(precio);
                
            }
            
        }
        return (f);
    }
