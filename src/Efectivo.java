    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
        // TODO add your handling code here:
        float f = Float.parseFloat(txtPaga.getText());
        float algo = f-Globales.getPrecioTotal();
        float neg = Globales.getPrecioTotal()-f;
        String s = Float.toString(algo);
        
        
        if (algo>=0){
            PrinterJob printerJob = PrinterJob.getPrinterJob();
            printerJob.setPrintable(txtEfectivo.getPrintable(null, null));
        
            if(printerJob.printDialog()){
            try{
                printerJob.print();
            }catch(PrinterException ex){
                System.out.println("Error: "+ex);
                }
            }
            JOptionPane.showMessageDialog(null,"Compra realizada \nVuelto: $"+algo,"Completo",JOptionPane.INFORMATION_MESSAGE);
            Ventana abrir= new Ventana();
            abrir.setVisible(true);
            this.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(null,"Falta: $"+neg,"Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPagarActionPerformed

