    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String falta_algo = "";
        if (Nombre.getText().trim().equals("")) {
            falta_algo = falta_algo + "Falta el nombre\n";
        }
        if (DNI.getText().trim().equals("")) {
            falta_algo = falta_algo + "Falta el DNI\n";
        }
        if (Direccion.getText().trim().equals("")) {
            falta_algo = falta_algo + "Falta la direccion\n";
        }
        if (Mail.getText().trim().equals("")) {
            falta_algo = falta_algo + "Falta el mail\n";
        }
        if (CP.getText().trim().equals("")) {
            falta_algo = falta_algo + "Falta el codigo postal\n";
        }
        if (cmbMetodo.getSelectedItem().equals("Seleccione forma de pago")) {
            falta_algo = falta_algo + "Falta el metodo de pago\n";
        }
        if (Nombre.getText().trim().equals("") || DNI.getText().trim().equals("") || Direccion.getText().trim().equals("") || Mail.getText().trim().equals("") || CP.getText().trim().equals("") || cmbMetodo.getSelectedItem().toString().equals("Seleccione forma de pago")) {
            JOptionPane.showMessageDialog(null, falta_algo, "Error", JOptionPane.ERROR_MESSAGE);

        }

        try {
            if (Nombre.getText().trim().equals("") || DNI.getText().trim().equals("") || Direccion.getText().trim().equals("") || Mail.getText().trim().equals("") || CP.getText().trim().equals("") || cmbMetodo.getSelectedItem().toString().equals("Seleccione forma de pago")) {

            } else {
                File archivo = new File("persona.txt");
                FileWriter escribir = new FileWriter(archivo, true);
                escribir.write((String) "\nNombre:  " + Nombre.getText().trim() + "\n"
                        + "Dni:  " + DNI.getText().trim() );

                escribir.close();
            }

        } catch (Exception e) {
        }

        if (cmbMetodo.getSelectedItem().toString().equals("Credito")) {
            Credito abrir = new Credito();
            abrir.setVisible(true);
            this.setVisible(false);
        } else if (cmbMetodo.getSelectedItem().toString().equals("Efectivo")) {
            Efectivo abrir = new Efectivo();
            abrir.setVisible(true);
            this.setVisible(false);
        }

        inicializar();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void CPKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_CPKeyReleased
        // TODO add your handling code here:
        byte cero = '0';
        byte nueve = '9';
        char llega=evt.getKeyChar();
        String string ="";
        
        
        char[] a = CP.getText().toCharArray();
        for (int i = 0; i < a.length; i++) {
            if (a[i]>=cero && a[i]<=nueve){
               string+=a[i]; 
            }
            if (i>=3)
                break;
        }
        CP.setText(string);
    }//GEN-LAST:event_CPKeyReleased

